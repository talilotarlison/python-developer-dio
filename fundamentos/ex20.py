# Um operador binário é um tipo de operador que atua sobre dois operandos. Ele realiza uma operação que combina ou manipula esses dois valores. Exemplos comuns de operadores binários em Python incluem:
# Definindo duas variáveis
a = 10
b = 5
# Aritméticos:

# Adição: +
# Subtração: -
# Multiplicação: *
# Divisão: /
# Módulo: %
# Comparação:

# Aritméticos
soma = a + b          # Adição
subtracao = a - b     # Subtração
multiplicacao = a * b # Multiplicação
divisao = a / b       # Divisão
modulo = a % b        # Módulo

# Igualdade: ==
# Diferença: !=
# Maior que: >
# Menor que: <
# Maior ou igual a: >=
# Menor ou igual a: <=
# Lógicos:


# Comparação
igual = a == b        # Igualdade
diferente = a != b    # Diferença
maior = a > b         # Maior que
menor = a < b         # Menor que

# E lógico: and
# Ou lógico: or
# Não lógico: not (embora not seja unário, and e or são binários)


# Exibindo os resultados
print(f"Soma: {soma}")
print(f"Subtração: {subtracao}")
print(f"Multiplicação: {multiplicacao}")
print(f"Divisão: {divisao}")
print(f"Módulo: {modulo}")
print(f"Igual: {igual}")
print(f"Diferente: {diferente}")
print(f"Maior: {maior}")
print(f"Menor: {menor}")


