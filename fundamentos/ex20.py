# Um operador unário é um tipo de operador que atua sobre um único operando. Em outras palavras, ele realiza uma operação em apenas um valor. Exemplos comuns de operadores unários em Python incluem:

# Negação: O operador - pode ser usado para inverter o sinal de um número. Por exemplo, -x.
# Incremento ou Decremento: Em algumas linguagens, como C, operadores como ++ ou -- são usados para incrementar ou decrementar um valor. Em Python, você precisaria usar x += 1 ou x -= 1.
# Operador de identidade: O operador + pode ser usado para indicar que um número é positivo, como em +x.
